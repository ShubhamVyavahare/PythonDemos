# if statements
result = 100
if result == 100:
    print("Inside If\n")

# if...else
if result == 99:
    print("Inside If\n")
else:
    print("Inside else\n")

# if...elif...else
if result == 99:
    print("Inside If\n")
elif result == 100:
    print("Inside elif\n")
else:
    print("Inside else\n")